# ========== СТАТИСТИКА БАЗЫ ФРАЗ ==========

def get_total_phrases_count() -> dict:
    """
    Подсчитать общее количество фраз в базе
    
    Returns:
        dict: {
            'total': общее количество,
            'by_category': {категория: количество},
            'by_style': {стиль: количество}
        }
    """
    total = 0
    by_category = {}
    by_style = {"friendly": 0, "business": 0, "sarcastic": 0, "motivating": 0}
    
    for category, styles in TEMPLATES.items():
        category_total = 0
        for style, phrases in styles.items():
            count = len(phrases)
            category_total += count
            if style in by_style:
                by_style[style] += count
        by_category[category] = category_total
        total += category_total
    
    return {
        "total": total,
        "by_category": by_category,
        "by_style": by_style
    }


