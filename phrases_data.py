def get_time_based_emoji() -> str:
    """Получить эмодзи в зависимости от времени суток"""
    hour = datetime.now().hour
    if 6 <= hour < 12:
        return random.choice(["🌅", "☀️", "🌤️", "☕", "🥐"])
    elif 12 <= hour < 17:
        return random.choice(["☀️", "🌞", "😎", "🍕", "🥗"])
    elif 17 <= hour < 21:
        return random.choice(["🌅", "🌆", "🍷", "🎮", "📺"])
    else:
        return random.choice(["🌙", "⭐", "🌃", "🦉", "😴"])

def get_seasonal_emoji() -> str:
    """Получить эмодзи по сезону"""
    month = datetime.now().month
    if month in [12, 1, 2]:
        return random.choice(["❄️", "☃️", "🎄", "🧣", "☕"])
    elif month in [3, 4, 5]:
        return random.choice(["🌸", "🌱", "🌷", "🦋", "☔"])
    elif month in [6, 7, 8]:
        return random.choice(["☀️", "🏖️", "🍦", "🌻", "🕶️"])
    else:
        return random.choice(["🍂", "🎃", "🌧️", "🍁", "☔"])

